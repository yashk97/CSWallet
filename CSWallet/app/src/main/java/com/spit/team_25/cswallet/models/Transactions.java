package com.spit.team_25.cswallet.models;

public class Transactions {
    private String TID, Status, Transaction_with, Amount, Timestamp;

    public String getTID(){return TID;}

    public void setTID(){this.TID = TID;}

    public String getStatus(){return Status;}

    public void setStatus(){this.Status = Status;}

    public String getTransaction_with(){return Transaction_with;}

    public void setTransaction_with(){this.Transaction_with = Transaction_with;}

    public String getAmount(){return Amount;}

    public void setAmount(){this.Amount = Amount;}

    public String getTimestamp(){return Timestamp;}

    public void setTimestamp(){this.Timestamp = Timestamp;}

}